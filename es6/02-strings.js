const hello = 'Hello';
const world = 'World';

const epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

//template literals
const epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

//multi-line strings
const lorem = 'This is a line \n' + 'This is a new line';
console.log(lorem);

const lorem2 = `This is a line
EThis is a new line`;
console.log(lorem2);
